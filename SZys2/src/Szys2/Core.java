package Szys2;

public class Core 
{
    public int Add(int a,int b,int c)//加法
    {
        
        return a+b+c;
    }
    public int Subtraction(int a,int b,int c)//减法
    {
        
        return a-b-c;
    }
    public int multiplication(int a,int b,int c)//减法
    {
        return a*b*c;
    }
    public int Division(int a,int b,int c)//除法
    {
       
        return a/b/c;
    }
}
